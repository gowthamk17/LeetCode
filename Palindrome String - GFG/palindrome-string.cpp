class Solution{
public:	
	
	
	int isPalindrome(string S)
	{
	    // Your code goes here
	    int i = 0, len = S.length()-1, result = 1;
	    while(i <= len) {
	        if(S[i] != S[len]) {
	            result = 0;
	            break;
	        }
	        i++;
	        len--;
	    }
	    return result;
	}

};

